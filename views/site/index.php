                var height = w/rate * 1.76;
            }else if( w > 982 && w < 1200){
                var height = w/rate * (1+w/2000);
            }else if( w >= 1200){
                var height = w/rate;
            }
            var top = height * r1;
            var bottom = height * r2 - top;
            div.css('height', height+'px');
            div.css('padding-top', top+'px');
            p.css('padding-bottom', bottom+'px');
        }
